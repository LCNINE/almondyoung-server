export function getIconComponent(name?: string): LucideIcon | null {
  if (!name) return null;
  return iconMap[name] ?? null;
}

export function IconComponent({
  name,
  className,
}: {
  name?: string;
  className?: string;
}) {
  const Icon = getIconComponent(name);
  return Icon ? <Icon className={className ?? 'size-4'} /> : null;
}
